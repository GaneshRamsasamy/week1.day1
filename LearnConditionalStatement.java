public class LearnConditionalStatement {

	public static void main(String[] args) {
		int age = 15;
		
		if(age < 18) {
		System.out.println("Minor");
		}
		
		if(age >= 18) {
			System.out.println("Major");
		}
		
		System.out.println("End of the program");
		
	}
	

}
